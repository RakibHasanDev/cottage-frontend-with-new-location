// Helper function to normalize phone numbers (remove non-digit characters)
const normalizePhone = (num) => num.replace(/\D/g, "");

// Set for faster lookup of existing numbers
const normalizedExistingNumbers = new Set(
  existingPhoneNumbers.map(normalizePhone)
);

// Function to validate phone number
export const validatePhoneNumber = (phone) => {
  const normalizedPhone = normalizePhone(phone);

  // Regex pattern to match US phone numbers (10 digits)
  const phonePattern = /^\d{10}$/;

  if (!phonePattern.test(normalizedPhone)) {
    return "Please enter a valid 10-digit US phone number";
  }

  if (normalizedExistingNumbers.has(normalizedPhone)) {
    return "Phone number already exists";
  }

  return true; // Validation passed
};
